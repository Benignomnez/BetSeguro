/**
 * Get the logo URL for a team based on its code
 * @param code Team code (e.g., 'NYM')
 * @returns URL to the team logo, or a placeholder if not found
 */
export function getTeamLogo(code: string): string {
  // First, try the exact code
  if (teamLogos[code]) {
    return teamLogos[code];
  }

  // If not found, try checking case-insensitive
  const upperCode = code.toUpperCase();
  if (teamLogos[upperCode]) {
    return teamLogos[upperCode];
  }

  // Return a placeholder if not found
  return "/placeholder.svg";
}
